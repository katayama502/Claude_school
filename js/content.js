/* ------- AppData.TOPICS に contentJa をマージ ------- */
(function mergeContent() {
  var topics = window.AppData && window.AppData.TOPICS;
  if (!topics) {
    console.warn('content.js: AppData.TOPICS が見つかりません。data.js より後に読み込んでください。');
    return;
  }
  var merged = 0;
  Object.keys(TOPIC_CONTENT).forEach(function(id) {
    if (topics[id]) {
      topics[id].contentJa = TOPIC_CONTENT[id];
      merged++;
    }
  });
  console.info('content.js: ' + merged + ' トピックに日本語コンテンツをマージしました。');
})();
